#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (*src == '\0')
			dst[i++] = '\0';
		else
			dst[i++] = *src++;
	}
	return (dst);
}
